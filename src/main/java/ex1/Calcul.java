public class Calcul {

	/**
	 * Additionne a et b
	 * @param a premier parametre additionné
	 * @param b deuxième paramètre additionné
	 * @return resultat de l'addition de a et b
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	
	/**
	 * Soustrait b de a
	 * @param a premier paramètre.
	 * @param b deuxième paramètre (celui soustrait)
	 * @return a moins b
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
